export const featuredVehicles = vehicles.filter((v) => v.featured);

export const getVehicleBySlug = (slug?: string): Car | undefined => {
  if (!slug) return undefined;
  const normalizedSlug = slug.toLowerCase();
  return vehicles.find((v) => v.slug.toLowerCase() === normalizedSlug);
};

export const getVehiclesByCategory = (category: string): Car[] => {
  if (category === "all") return vehicles;
  return vehicles.filter((v) => v.category === category);
};
